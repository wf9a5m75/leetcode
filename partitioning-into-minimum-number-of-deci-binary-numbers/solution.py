class Solution:
    def minPartitions(self, n: str) -> int:
        """
        82734 - 11111 => 71623
        71623 - 11111 => 60512
        60412 - 10111 => 50302
        50302 - 10101 => 40201
        40201 - 10101 => 30100
        30100 - 10100 => 20000
        20000 - 10000 => 10000
        10000 - 10000 => 00000
        """
        return int(max(n))
